package miercoles13_04;

public class Animal {
	//lisa de atributos
	private int edad;
	private String nombre;
	private String raza;
	
	
	
	//lista de metodos
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getRaza() {
		return raza;
	}
	public void setRaza(String raza) {
		this.raza = raza;
	}

}
